# Inteiro
numero_inteiro = 10
print("Inteiro = ",numero_inteiro)

# Real
numero_decimal = 3.14
print("Real = ",numero_decimal)

print(type(numero_inteiro))
print(type(numero_decimal))

# Soma
a = 4
b = 2
soma = a + b
print("Soma = ",soma)

# Subtração
subtracao = a - b
print("Subtração = ",subtracao)

# Multiplicação
multiplicacao = a * b
print("Multiplicação = ",multiplicacao)

# Divisão
divisao = a / b
print("Divisão = ",divisao)

# Divisão inteira
divisao_inteira = a // b
print("Divisão inteira = ",divisao_inteira)

# Resto da divisão
resto = a % b
print("Resto da divisão = ",resto)

# Potência
potencia = a ** b
print("Potência = ",potencia)

# Raiz quadrada
raiz_quadrada = a ** 0.5
print("Raiz quadrada = ",raiz_quadrada)
